package br.com.integracaojr.entidade;

public class PlanoConta {
	private int entidade;
	private int eloexercicio;
	private String eloconta;
	private String elodescricao;
	private int elocontacorrente;
	private String ipmconta;
	private String ipmdescricao;
	private String ipmvinculo;

	public int getEntidade() {
		return entidade;
	}

	public void setEntidade(int entidade) {
		this.entidade = entidade;
	}

	public int getEloexercicio() {
		return eloexercicio;
	}

	public void setEloexercicio(int eloexercicio) {
		this.eloexercicio = eloexercicio;
	}

	public String getEloconta() {
		return eloconta;
	}

	public void setEloconta(String eloconta) {
		this.eloconta = eloconta;
	}

	public String getElodescricao() {
		return elodescricao;
	}

	public void setElodescricao(String elodescricao) {
		this.elodescricao = elodescricao;
	}

	public int getElocontacorrente() {
		return elocontacorrente;
	}

	public void setElocontacorrente(int elocontacorrente) {
		this.elocontacorrente = elocontacorrente;
	}

	public String getIpmconta() {
		return ipmconta;
	}

	public void setIpmconta(String ipmconta) {
		this.ipmconta = ipmconta;
	}

	public String getIpmdescricao() {
		return ipmdescricao;
	}

	public void setIpmdescricao(String ipmdescricao) {
		this.ipmdescricao = ipmdescricao;
	}

	public String getIpmvinculo() {
		return ipmvinculo;
	}

	public void setIpmvinculo(String ipmvinculo) {
		this.ipmvinculo = ipmvinculo;
	}

}
