package br.com.integracaojr.entidade;

public class FonteRecurso {
	private int entidade;
	private int elofonterecurso;
	private String elodescricao;
	private int ipmvinculo;
	private String ipmdescricao;

	public int getEntidade() {
		return entidade;
	}

	public void setEntidade(int entidade) {
		this.entidade = entidade;
	}

	public int getElofonterecurso() {
		return elofonterecurso;
	}

	public void setElofonterecurso(int elofonterecurso) {
		this.elofonterecurso = elofonterecurso;
	}

	public String getElodescricao() {
		return elodescricao;
	}

	public void setElodescricao(String elodescricao) {
		this.elodescricao = elodescricao;
	}

	public int getIpmvinculo() {
		return ipmvinculo;
	}

	public void setIpmvinculo(int ipmvinculo) {
		this.ipmvinculo = ipmvinculo;
	}

	public String getIpmdescricao() {
		return ipmdescricao;
	}

	public void setIpmdescricao(String ipmdescricao) {
		this.ipmdescricao = ipmdescricao;
	}

}
